package com.pms.petopia.domain;

public class Type {

  private int no;
  private String type;
  private Species bigType;

  public int getNo() {
    return no;
  }
  public void setNo(int no) {
    this.no = no;
  }
  public String getType() {
    return type;
  }
  public void setType(String type) {
    this.type = type;
  }
  public Species getBigType() {
    return bigType;
  }
  public void setBigType(Species bigType) {
    this.bigType = bigType;
  }


}
